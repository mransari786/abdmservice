package com.gov.nha.bis.server.dto;

public class partner_resp {
	private String name;
	private String total;
	private String total_linked;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getTotal_linked() {
		return total_linked;
	}
	public void setTotal_linked(String total_linked) {
		this.total_linked = total_linked;
	}
	

}
